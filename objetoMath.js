console.log(Math); // Objeto -> Object [Math] {}

console.log(Math.random()); // Devuelve un numero entre 0 y 1
console.log(Math.random() * 10 ); // Devuelve un numero entre 0 y 10
console.log(Math.max(34,5,87,45,234,89,678,93,2,46,98)); // Devuelve el mayor valor -> 678
console.log(Math.min(34,5,87,45,234,89,678,93,2,46,98)); // Devuelve el menor valor -> 2
console.log(Math.floor(Math.random() * 10)); //Redondea para abajo a entero
console.log(Math.PI); // Devuelve el nuemero PI = 3.141592653589793
console.log(Math.ceil(12.34)); // Redondea hacia arriba -> 13
console.log(Math.round(12.45)); // Redondea al entero mas cercano -> 12